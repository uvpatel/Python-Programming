a = float(input("Enter a number: "))
b = float(input("Enter another number: "))
          
# Arithmetic operators are used to perform arithmetic operations on variables and values.

print("a + b = ", a + b) # 34 + 22 = 56
print("a - b = ", a - b) # 34 - 22 = 12
print("a * b = ", a * b) # 34 * 22 = 748
print("a / b = ", a / b) # 34 / 22 = 1.5454545454545454
print("a // b = ", a // b) # 34 // 22 = 1
print("a % b = ", a % b) # 34 % 22 = 12 
print("a ** b = ", a ** b) # 34 ** 22 = 1.0000000000000002e+34 

