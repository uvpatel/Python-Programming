a = int(input("Enter a number: ")) # enter a number is a prompt. use prompt to ask the user for input

a += 3 # it adds 3 to a. so a = a + 3
print(a) # 34 + 3 = 37
a -= 3 # it subtracts 3 from a. so a = a - 3
print(a) # 37 - 3 = 34
a *= 3 # it multiplies a by 3. so a = a * 3
print(a) # 34 * 3 = 102
a /= 3 # it divides a by 3. so a = a / 3
print(a) # 102 / 3 = 34.0
a //= 3 # it divides a by 3 and returns the floor value. so a = a // 3
print(a) # 34.0 // 3 = 11.0
a %= 3 # it returns the remainder of a divided by 3. so a = a % 3
print(a) # 11.0 % 3 = 2.0
a **= 3 # it raises a to the power of 3. so a = a ** 3
print(a) # 2.0 ** 3 = 8.0
a &= 3 # it performs bitwise AND operation on a and 3. so a = a & 3
print(a) # 8.0 & 3 = 0.0
a |= 3 # it performs bitwise OR operation on a and 3. so a = a | 3
print(a) # 0.0 | 3 = 3.0
a ^= 3 # it performs bitwise XOR operation on a and 3. so a = a ^ 3
print(a) # 3.0 ^ 3 = 0.0
a >>= 3 # it performs right shift operation on a by 3. so a = a >> 3
print(a) # 0.0 >> 3 = 0.0
a <<= 3 # it performs left shift operation on a by 3. so a = a << 3
print(a) # 0.0 << 3 = 0.0

